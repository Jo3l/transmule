# Path-specific overrides for ambiguous translations
PATH_OVERRIDES = {
    # "Open" in ports and transmission/network contexts = "Açık" (adjective - port status)
    ("settings", "ports", "open"): "Açık",
    ("settings", "transmission", "network", "open"): "Açık",
}

def translate_string(s, path_parts=None):
    """Translate a single English string to Turkish."""
    if path_parts is not None and path_parts in PATH_OVERRIDES:
        return PATH_OVERRIDES[path_parts]
    if s in MAP:
        return MAP[s]
    return s


def translate_recursive(obj, path_parts=None):
    """Recursively translate all string values in a nested dict/list."""
    if isinstance(obj, dict):
        return {k: translate_recursive(v, path_parts + (k,) if path_parts else (k,)) for k, v in obj.items()}
    elif isinstance(obj, list):
        return [translate_recursive(v, path_parts) for v in obj]
    elif isinstance(obj, str):
        return translate_string(obj, path_parts)
    else:
        return obj


# First pass: simple string mapping
def simple_translate(obj):
    if isinstance(obj, dict):
        return {k: simple_translate(v) for k, v in obj.items()}
    elif isinstance(obj, list):
        return [simple_translate(v) for v in obj]
    elif isinstance(obj, str):
        return MAP.get(obj, obj)
    else:
        return obj

